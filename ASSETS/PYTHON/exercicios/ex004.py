

# Um programa que leia algo pelo teclado e mostre na tela seu tipo primitivo e todas as informações possíveis sobre ele

v = input('Escreva algo: ')
print(type(v))
print(v.isalnum)
print(v.isalpha)
print(v.isascii)
print(v.isdecimal)
print(v.isdigit)
print(v.islower)
print(v.isnumeric)
print(v.isidentifier)
print(v.isspace)